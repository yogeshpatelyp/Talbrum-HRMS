public class VacancyDataType {

    private String role, location, designation, posted_on, id, no_of_posts;

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getDesignation() {
        return designation;
    }

    public void setDesignation(String designation) {
        this.designation = designation;
    }

    public String getPostedOn() {
        return posted_on;
    }

    public void setPostedOn(String posted_on) {
        this.posted_on = posted_on;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNoOfPosts() {
        return no_of_posts;
    }

    public void setNoOfPosts(String no_of_posts) {
        this.no_of_posts = no_of_posts;
    }
}
